								<h3>Песенный жанр</h3>
							</header>
							<p class="white-font">В песенном творчестве татар есть несколько основных жанров. По тематике и функции песни бывают лирические, обрядовые, трудовые, игровые, колыбельные, а по форме и музыкальному стилю — протяжные (озын көй), короткие (кыска көй) песни и так называемые деревенские напевы (авыл көе).

							<p class="white-font"> Со второй половины XIX века в истории татарской музыкальной культуры начинается качественно новый этап. На фоне общих процессов интеграции татар (выработки общих норм литературного языка, консолидации разных субэтнических групп, которая в итоге привела к формированию современной нации) складывается общенациональный песенный стиль. Тогда в быт татар проникали новые музыкальные инструменты (например, гармоники), развивались новые жанры, сочинялись песни, которые известны до сих пор. Среди них — «Күбәләгем» («Бабочка моя»), «Алмагачлары» («Яблони»), «Баламишкин» («Дитя Мишкина»), «Сабан туе» («Сабантуй»), «Арча» («Арск»), «Сарман» («Сарманово»), «Минзәлә» («Мензелинск») и другие. Это, как правило, лирические, игровые и плясовые песни. По структуре они достаточно просты и однородны, основываются на восьми-, семислоговом стихе, имеют равнопульсирующий слогоритм. Этим они отличаются от песен старого, донационального слоя.
							<p class="white-font"> В песенном творчестве татар есть несколько основных жанров. По тематике и функции песни бывают лирические, обрядовые, трудовые, игровые, колыбельные, а по форме и музыкальному стилю — протяжные (озын көй), короткие (кыска көй) песни и так называемые деревенские напевы (авыл көе). Озын көй — протяжные лирические песни. Как правило, на них накладывали тексты десяти- или девятислогового строения. Классические примеры: «Кара урман» («Дремучий лес»), «Әллүки» (звукосочетание наподобие «баю-бай»), «Сибелә чәчәк» («Осыпаются цветы»), «Салкын чишмә» («Холодный родник»). Кыска көй — подвижные мелодии с четкой «квадратной» структурой и незначительным количеством распевов. Популярные примеры — «Күбәләгем» («Бабочка моя»), «Алмагачлары» («Яблони»), «Баламишкин» («Дитя Мишкина»), «Сабан туе» («Сабантуй»). Авыл көе — лирические песни умеренного темпа. В названиях нередко содержатся наименования мест их возникновения и распространения: бывают песни «Сарман», «Арча» и так далее. Часто исполняются коллективно, под аккомпанемент гармони.
