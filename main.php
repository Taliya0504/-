								<p> В развитии народной музыки выделяют три основных этапа: Древняя эпоха (архаический фольклор) — Средневековье (традиционный, или классический, фольклор) — современность (современный фольклор). Это универсальная схема, применимая для многих народов. В формировании татарской музыки на ранних этапах приняли участие тюркский и финно-угорский компоненты, в средневековый период — арабо-персидский, а в позднюю эпоху — европейский и русский. Аналогичные процессы происходили и в татарском языке: можно проследить общетюркскую основу, финно-угорскую лексику, арабо-персидские, русские и иностранные заимствования.</p>
								<p>Первую нотную запись татарских напевов сделал учитель музыки Иван Добровольский в 1816 году. Вероятно, в зарубежных архивах есть еще нотные рукописи, но, так или иначе, музыковеды располагают достоверной, документально зафиксированной информацией о татарских народных мелодиях лишь позднего периода. Музыкальные инструменты, в отличие от напевов, осязаемы и служат материальными свидетельствами ушедшей эпохи. Духовые (глиняные свистульки) и струнные инструменты типа гуслей археологи находили во время раскопок на территории Булгарского государства. Но подобные находки — редкость, потому что инструменты, сделанные из недолговечных материалов (дерево, тростник), практически не сохраняются в почве.</p>
