								<h3>Музыкальные инструменты: 
									гармонь и скрипка</h3>
							</header>
							<p class="white-font">Сегодня татарская инструментальная музыка ассоциируется у людей с гармоникой, хотя в быт татар она вошла только во второй половине XIX века. Правда, она сразу обрела невероятную популярность и получила повсеместное распространение, отодвинув на задний план предшествующие курай, скрипку, кубыз (тимер-кубыз — металлический инструмент в форме дуги с вибрирующей пластиной) и другие. 
						
							<p class="white-font"> Татары немного изменили конструкцию гармоники, приспособив ее под пентатонные одноголосные мелодии (пентатоника — ладовая основа татарской музыки). В результате появились национальные разновидности гармоники. А еще татарские музыканты выработали собственную манеру игры: если русские широко использовали гармонические возможности гармошки (играли на ней аккорды, поддерживающие мелодию голоса), то татары освоили ее преимущественно как мелодический инструмент, стремясь воспроизвести орнаментальные мелодии в высоком регистре, как это делали раньше на курае или народной скрипке. Татары тоже применяли аккорды, но для них они не имели принципиального значения. Сохранялся монодийный (одноголосный) тип мышления, то есть мы видим, что инструмент был новым, а восприятие музыки осталось прежним.
							<p class="white-font"> Вероятно, гармонь стала популярной благодаря акустическим и конструктивным свойствам: громкий звук, яркий тембр, долговечный материал, портативность, устойчивый строй. Для сравнения: дерево или полый стебель растения, из которых делали кураи, со временем рассыхались, трескались. У тимер-кубыза — звук тихий, слабый, а тонкая пластинка в процессе эксплуатации ломалась. Гусли, которые использовали кряшены, постоянно требовали подстройки.
								<p class="white-font"> До гармоники традиционным для татар инструментом была скрипка. Об этом, например, поется в русской народной песне: «Приведи-ка, матушка, татарина с скрыпкою, мордвина с волынкою» (кстати, текст этой песни в Болдино записал Александр Сергеевич Пушкин). Люди, далекие от фольклора, думают, что скрипка — не татарский инструмент, а европейский, вспоминают Паганини, мастеров Страдивари и Амати. Но людей вводит в заблуждение то, что они представляют фабричный инструмент, хотя раньше скрипки часто изготавливали из хорошо высушенной еловой или яблоневой древесины кустарным способом, а на смычок натягивали конский волос. При игре инструмент держали не возле плеча, а вертикально, с упором на колено. Именно так играли в прошлом на кыл-кубызе — древнем струнно-смычковом инструменте тюркоязычных народов, предшественнике современной народной скрипки. В некоторых ареалах скрипку называли «каз-муен» («гусиная шея») — такое название я слышала в 2016 году в Тетюшском районе Татарстана. Но, к сожалению, традиция игры на скрипке у татар сегодня полностью утрачена.
