						<h2>Музыкальная культура татарского народа</h2>
						<p>Музыкальная культура также переживает замечательный подъем, перед которым кажутся незначительными успехи, достигнутые в восстановительный период. Татарская музыкальная молодежь получила в это время серьезное образование у русских преподавателей в Московской консерватории, где по решению Советского правительства была открыта татарская оперная студия. Там велась всесторонняя подготовка по всем видам музыкальных специальностей: композиторов, режиссеров, певцов, оркестрантов, артистов балета и пр. Почти весь актив молодых музыкантов, подготовленных в Казанском музыкальном техникуме, проходил обучение в Москве. Там учились такие исполнители, как С. Айдаров, С. Садыкова, М. Булатова, Х. Забирова, А. Измайлова, Г. Кайбицкая, Ф. Насретдинов, М. Рахманкулова и многие другие; композиторы С. Сайдашев, Н. Жиганов, Дж. Файзи, 3. Хабибуллин, Ф. Яруллин и т.д.

							Уже в 1939 г. Казань порадовалась прекрасным результатам, достигнутым татарскими музыкантами, обучавшимися в Москве. Молодой композитор Н. Жиганов закончил свою первую оперу «Качкын» («Беглец»). Это было крупным событием, означавшим начало нового периода в истории татарской музыки. «Качкын» явился оперой вполне национальной, написанной талантливым высококультурным татарским композитором, выходцем из рабочей семьи. Период «коллективных», «фольклорных» опер, типа «Сания», закончился. К премьере «Качкын» завершилась и организация Татарского государственного театра оперы и балета, торжественно открытого в 1939 году.
							
							Эта премьера прошла как народное торжество, исполнители удостоились почетных званий: народных артистов Татарской АССР — А. Измайлова и Г. Кайбицкая,
							заслуженных деятелей искусств Татарской АССР — Н. Жиганов, Г. И. Литинский (проф. Московской консерватории, руководитель татарской студии) и С. С. Бергольц (первый дирижер и художественный руководитель театра). В довоенные годы С. Сайдашев написал несколько музыкальных произведений к спектаклям: «Бишбуляк» Т. Гиззата, «Кузляр» Фаткуллина и др.
							
							В работе оперного театра ярко проявлялось содружество татарских и русских деятелей сцены — черта, столь свойственная всем областям жизни в советскую эпоху. Часто в одних и тех же ролях выступали и татарские и русские артисты, оперы ставились русскими и татарскими режиссерами.
							
							В 1940 г. была поставлена вторая опера Н. Жиганова «Ирек» («Свобода») на тему: Октябрь в татарской деревне (либретто 3. Сафина), а в 1941 г.- третья его опера «Алтынчэч» («Золотоволосая») на сюжет татарской народной легенды (либретто написано М. Джалилем). За эту оперу композитор удостоился Сталинской премии.
							
							Казанские композиторы за эти годы создали выдающиеся произведения во многих музыкальных жанрах: опера «Галиябану» М. Музаффарова, балет «Шурале» Ф. Яруллина на сюжет сказки Габдуллы Тукая, сюита «Сабантуй» А. Ключарева, крупные симфонические вещи, сюиты, увертюры и пр. (В. Виноградов, А. Ключарев, М. Музаффаров, Н. Жиганов и другие), много лирических песен и художественных обработок народных песен (Сайдашева Салиха Замалетдиновича, Файзи Джаудата Харисовича,Хабибуллина Загида Валеевича.).</p>
