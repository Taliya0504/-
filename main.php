								<h3>Ислам и музыка: 
									татарская народная традиция чтения Корана</h3>
							</header>
							<p class="white-font">Искусство чтения Корана — самобытная и важная часть татарской традиционной музыкальной культуры. Истоки традиции восходят к Булгарскому периоду истории, времени принятия ислама (официальной датой считается 922 год, хотя, по некоторым данным, это произошло раньше — в VIII–IX веках).

							<p class="white-font"> Исполнители коранических распевов ориентируются на таджвид — науку правильного чтения Корана в соответствии с фонетикой арабского языка. Роль играют и музыкальные предпочтения чтецов. Информанты говорят, что даже в мусульманских учебных заведениях конкретным напевам не обучают, поэтому каждый решает эту творческую задачу по-своему. Люди слушают кассеты, диски, обращаются к записям в интернете, и в последние годы все чаще (особенно молодые священнослужители) интонируют на арабский манер. Исполнители старшего возраста (60−80 лет) традиционно опираются на пентатонику, в их манере прослеживается прочная связь с народно-песенными истоками.
							<p class="white-font"> В некоторых толкованиях Корана утверждается, что исполнение и прослушивание музыки — грех. Но согласно нашим экспедиционным опросам и наблюдениям, население считает, что музыка не противоречит нормам ислама. Осуждается все, что связано с грехом или доводит до греха, — праздное времяпрепровождение или распитие спиртных напитков. А пение и игра на инструментах, тем более если они служат благим целям, наоборот, приветствуются. При этом в некоторых деревнях пожилые женщины отказывались петь на сцене клуба, объясняя это тем, что в их возрасте это неприлично. А где-то муллы или абыстай участвуют в фольклорных ансамблях и активно выступают на концертных площадках. Еще я знаю одного священнослужителя, который даже организовал кружок по изготовлению кураев. Во время занятий он не только посвящает детей в тонкости ремесла, но и проводит беседы об исламе, поясняет смысл ритуалов, молитв и так далее. То есть он поставил музыку на службу религии.
